  // For all other languages not explicitly translated, fall back to English
  // with the language's own name shown in the dropdown.

  const _fallback = TRANSLATIONS.en;

  /**
   * Get the translation object for a given language code.
   * Falls back to English for untranslated languages.
   */
  function getTranslations(langCode) {
    return TRANSLATIONS[langCode] || _fallback;
  }

  /**
   * Get a specific translated string.
   */
  function t(langCode, key) {
    const trans = getTranslations(langCode);
    return trans[key] || _fallback[key] || key;
  }

  /**
   * Get the full list of supported languages for dropdown rendering.
   */
  function getLanguageList() {
    return LANGUAGES;
  }

  return { t, getTranslations, getLanguageList, LANGUAGES };
